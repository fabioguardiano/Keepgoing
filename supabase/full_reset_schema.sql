-- Este script cria todas as tabelas do zero com suporte a Multi-Tenancy (company_id)

-- 0. TABELA DE EMPRESAS (Necessária para referências)
CREATE TABLE IF NOT EXISTS companies (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    document TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

